ll dist(pll a, pll b){
    ll d = (a.fi - b.fi)*(a.fi - b.fi) + (a.se - b.se)*(a.se - b.se);
    return d;
}

void solve(){   
    pll a, b, c;
    cin >> a.fi >> a.se;
    cin >> b.fi >> b.se;
    cin >> c.fi >> c.se;

    vll d(3);
    d[0] = dist(a, b);
    d[1] = dist(b, c);
    d[2] = dist(a, c);
    
    sort(d.begin(), d.end());
    
    if(d[2] == d[0] + d[1]){
        cout << "Yes\n";
        return;
    } else{
        cout << "No\n";
        return;
    }
}   


