bool cmp(pii a, pii b){
    if(a.fi != b.fi) return a.fi < b.fi;
    return a.se < b.se;
}

bool check(int m){
    int cur_idx = 0;
    set<int> se;

    for(int i =0; i <v.sz; i++){
        while(cur_idx < v.sz && v[cur_idx].fi + m <= v[i].fi){
            se.insert(v[cur_idx].se);
            cur_idx++;
        }

        auto it = se.lower_bound(v[i].se + m);
        if(it != se.end()) return true;

        auto it_2 = se.upper_bound(v[i].se - m);
        if(it_2 != se.begin()) return true;
    }

    return false;
}

